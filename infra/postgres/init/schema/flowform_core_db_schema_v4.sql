CREATE TABLE project_invitations (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    project_id BIGINT NOT NULL REFERENCES projects (id) ON DELETE CASCADE,
    invited_email TEXT NOT NULL,
    role_id BIGINT REFERENCES project_roles (id) ON DELETE SET NULL,
    invited_by_user_id BIGINT REFERENCES users (id) ON DELETE SET NULL,
    invite_message TEXT,
    status TEXT NOT NULL DEFAULT 'pending',
    expires_at TIMESTAMPTZ,
    accepted_by_user_id BIGINT REFERENCES users (id) ON DELETE SET NULL,
    accepted_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    CONSTRAINT ck_project_invitations_status_valid
        CHECK (status IN ('pending', 'accepted', 'declined', 'revoked')),

    -- partial unique: only one pending invite per email per project
    -- (accepted/declined/revoked rows are kept for history and don't block re-invite)

    CONSTRAINT fk_project_invitations_role_same_project
        FOREIGN KEY (project_id, role_id)
        REFERENCES project_roles (project_id, id),

    CONSTRAINT ck_project_invitations_email_len
        CHECK (char_length(btrim(invited_email)) BETWEEN 1 AND 254),

    CONSTRAINT ck_project_invitations_message_len
        CHECK (invite_message IS NULL OR char_length(btrim(invite_message)) BETWEEN 1 AND 500),

    CONSTRAINT ck_project_invitations_accepted_fields
        CHECK (
            status <> 'accepted'
            OR (accepted_by_user_id IS NOT NULL AND accepted_at IS NOT NULL)
        )
);

CREATE UNIQUE INDEX uq_project_invitations_pending_project_email
    ON project_invitations (project_id, invited_email)
    WHERE status = 'pending';

CREATE INDEX ix_project_invitations_project ON project_invitations(project_id);
CREATE INDEX ix_project_invitations_email ON project_invitations(invited_email);
