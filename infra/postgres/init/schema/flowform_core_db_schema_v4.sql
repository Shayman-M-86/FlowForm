    identity_id UUID NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    CONSTRAINT uq_project_participants_project_subject
        UNIQUE (project_id, project_subject_id),

    -- Composite UNIQUE so survey_links can target a participant within its
    -- project via a same-project composite FK.
    CONSTRAINT uq_project_participants_project_id_id
        UNIQUE (project_id, id),

    CONSTRAINT fk_project_participants_subject_same_project
        FOREIGN KEY (project_id, project_subject_id)
        REFERENCES project_subjects(project_id, id)
        ON DELETE CASCADE,

    -- The participant's identity must belong to the participant's subject
    -- (and the same project). This is the guarantee that lets a link reach a
    -- subject + identity through the participant alone.
    CONSTRAINT fk_project_participants_identity_same_subject
        FOREIGN KEY (project_id, project_subject_id, identity_id)
        REFERENCES project_subject_identities(project_id, project_subject_id, id)
        ON DELETE CASCADE
);

-- =========================================
-- PUBLIC LINKS
-- =========================================
-- Bearer links store token hashes only.

CREATE TABLE survey_links (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),

    project_id BIGINT NOT NULL REFERENCES projects(id) ON DELETE CASCADE,
    survey_id BIGINT NOT NULL,
    name TEXT NOT NULL,
    token_prefix TEXT NOT NULL,
    token_hash TEXT NOT NULL,

    is_active BOOLEAN NOT NULL DEFAULT TRUE,

    -- general:       anonymous; anyone with the URL; no assigned participant.
    -- private:       no sign-in required; bearer-token possession is the proof
    --                of identity; must be assigned to a participant.
    -- authenticated: Auth0 sign-in required; must be assigned to a participant.
    link_type TEXT NOT NULL DEFAULT 'general',

    -- How the assignment was created. Always recorded, on every link:
    --   manual    — assigned by a human through the frontend.
    --   automated — created by a machine process.
    -- This is informational; it does not change which columns must be set.
    assignment_source TEXT NOT NULL,

    -- The sole assignment. A participant always resolves to a subject AND an
    -- identity (enforced on project_participants), so the link reaches both
    -- through this column alone. The assigned email is derived from the
    -- participant's identity by joining through project_participants.
    assigned_participant_id UUID,

    expires_at TIMESTAMPTZ,
    used_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    CONSTRAINT uq_survey_links_token_hash
        UNIQUE (token_hash),

    CONSTRAINT uq_survey_links_survey_id_id
        UNIQUE (survey_id, id),

    CONSTRAINT uq_survey_links_project_id_id
        UNIQUE (project_id, id),

    CONSTRAINT uq_survey_links_survey_id_token_prefix
        UNIQUE (survey_id, token_prefix),

    CONSTRAINT fk_survey_links_survey_same_project
        FOREIGN KEY (project_id, survey_id)
        REFERENCES surveys(project_id, id)
        ON DELETE CASCADE,

    -- Assigned participant must belong to the link's project. RESTRICT is
    -- intentional: do not silently turn an assigned link into a general one.
    CONSTRAINT fk_survey_links_assigned_participant_same_project
        FOREIGN KEY (project_id, assigned_participant_id)
        REFERENCES project_participants(project_id, id)
        ON DELETE RESTRICT,

    CONSTRAINT ck_survey_links_token_prefix_len
        CHECK (char_length(token_prefix) BETWEEN 8 AND 32),

    CONSTRAINT ck_survey_links_token_hash_format
        CHECK (token_hash ~ '^[0-9a-f]{64}$'),

    CONSTRAINT ck_survey_links_name_len
        CHECK (char_length(btrim(name)) BETWEEN 1 AND 120),

    CONSTRAINT ck_survey_links_link_type_valid
        CHECK (link_type IN ('general', 'private', 'authenticated')),

    -- Always recorded, regardless of link_type or whether a participant is set.
    CONSTRAINT ck_survey_links_assignment_source_valid
        CHECK (assignment_source IN ('manual', 'automated')),

    -- A general link carries no assigned participant.
    CONSTRAINT ck_survey_links_general_has_no_assignment
        CHECK (
            link_type <> 'general'
            OR assigned_participant_id IS NULL
        ),

    -- A private or authenticated link must be assigned to a participant.
    CONSTRAINT ck_survey_links_assigned_requires_participant
        CHECK (
            link_type = 'general'
            OR assigned_participant_id IS NOT NULL
        ),

    -- Single-use is derived from carrying a participant assignment.
    CONSTRAINT ck_survey_links_used_at_requires_assignment
        CHECK (
            used_at IS NULL
            OR assigned_participant_id IS NOT NULL
        )
);

CREATE TABLE submission_sessions (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    project_id BIGINT NOT NULL REFERENCES projects(id) ON DELETE CASCADE,
    survey_id BIGINT NOT NULL,
    survey_version_id BIGINT NOT NULL,
    response_store_id BIGINT NOT NULL REFERENCES response_stores(id) ON DELETE RESTRICT,
    link_id UUID REFERENCES survey_links(id) ON DELETE SET NULL,
    project_subject_id UUID REFERENCES project_subjects(id) ON DELETE SET NULL,
    browser_session_token_hash BYTEA NOT NULL,
    linkage_key_version SMALLINT NOT NULL DEFAULT 1,
    session_status TEXT NOT NULL DEFAULT 'in_progress',
    started_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    completed_at TIMESTAMPTZ,
    expires_at TIMESTAMPTZ NOT NULL,
    last_activity_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    CONSTRAINT uq_submission_sessions_browser_session_token_hash
        UNIQUE (browser_session_token_hash),

    CONSTRAINT uq_submission_sessions_id_survey_version_id
        UNIQUE (id, survey_version_id),

    CONSTRAINT uq_submission_sessions_project_id_id
        UNIQUE (project_id, id),

    CONSTRAINT uq_submission_sessions_id_project_subject_id
        UNIQUE (id, project_subject_id),

    CONSTRAINT ck_submission_sessions_browser_session_token_hash_len
        CHECK (length(browser_session_token_hash) >= 32),

    CONSTRAINT ck_submission_sessions_linkage_key_version_valid
        CHECK (linkage_key_version > 0),

    CONSTRAINT ck_submission_sessions_session_status_valid
        CHECK (session_status IN ('in_progress', 'completed', 'abandoned')),

    CONSTRAINT ck_submission_sessions_completed_at_consistent
        CHECK ((session_status = 'completed') = (completed_at IS NOT NULL)),

    CONSTRAINT ck_submission_sessions_completed_at_after_started_at
        CHECK (
            completed_at IS NULL
            OR completed_at >= started_at
        ),

    CONSTRAINT ck_submission_sessions_expires_at_after_started_at
        CHECK (expires_at > started_at),

    CONSTRAINT ck_submission_sessions_last_activity_at_after_started_at
        CHECK (last_activity_at >= started_at),

    CONSTRAINT ck_submission_sessions_completed_before_last_activity
        CHECK (
            completed_at IS NULL
            OR completed_at <= last_activity_at
        ),

    CONSTRAINT fk_submission_sessions_survey_same_project
        FOREIGN KEY (project_id, survey_id)
        REFERENCES surveys(project_id, id)
        ON DELETE CASCADE,

    CONSTRAINT fk_submission_sessions_version_same_survey
        FOREIGN KEY (survey_id, survey_version_id)
        REFERENCES survey_versions(survey_id, id)
        ON DELETE RESTRICT,


    CONSTRAINT fk_submission_sessions_store_same_project
        FOREIGN KEY (project_id, response_store_id)
        REFERENCES response_stores(project_id, id),


    CONSTRAINT fk_submission_sessions_link_same_survey
        FOREIGN KEY (survey_id, link_id)
        REFERENCES survey_links(survey_id, id),


    CONSTRAINT fk_submission_sessions_project_subject_same_project
        FOREIGN KEY (project_id, project_subject_id)
        REFERENCES project_subjects(project_id, id)
);

CREATE TABLE submission_events (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    session_id UUID NOT NULL,
    survey_version_id BIGINT NOT NULL,
    event_type TEXT NOT NULL,
    question_node_id UUID REFERENCES survey_questions(id) ON DELETE SET NULL,
    received_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    CONSTRAINT ck_submission_events_event_type_valid
        CHECK (event_type IN ('session_started', 'question_viewed', 'answer_saved', 'session_completed')),

    CONSTRAINT ck_submission_events_question_required_for_question_events
        CHECK (
            event_type NOT IN ('question_viewed', 'answer_saved')
            OR question_node_id IS NOT NULL
        ),

    CONSTRAINT ck_submission_events_question_absent_for_session_events
        CHECK (
            event_type NOT IN ('session_started', 'session_completed')
            OR question_node_id IS NULL
        ),

    CONSTRAINT fk_submission_events_session_version
        FOREIGN KEY (session_id, survey_version_id)
        REFERENCES submission_sessions(id, survey_version_id)
        ON DELETE CASCADE,

    CONSTRAINT fk_submission_events_question_node_same_version
        FOREIGN KEY (survey_version_id, question_node_id)
        REFERENCES survey_questions(survey_version_id, id)
);

